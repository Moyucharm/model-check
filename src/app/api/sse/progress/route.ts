// GET /api/sse/progress - Server-Sent Events for detection progress

import { NextRequest } from "next/server";
import { subscribeProgress } from "@/lib/queue/progress-bus";

export const dynamic = "force-dynamic";
export const runtime = "nodejs";

export async function GET(request: NextRequest) {
  const encoder = new TextEncoder();

  let isConnected = true;
  let heartbeatInterval: NodeJS.Timeout | null = null;
  let unsubscribe: (() => void) | null = null;

  const cleanup = () => {
    if (!isConnected) {
      return;
    }
    isConnected = false;

    if (heartbeatInterval) {
      clearInterval(heartbeatInterval);
      heartbeatInterval = null;
    }

    if (unsubscribe) {
      unsubscribe();
      unsubscribe = null;
    }
  };

  const stream = new ReadableStream({
    start(controller) {
      controller.enqueue(
        encoder.encode(`data: ${JSON.stringify({ type: "connected", timestamp: Date.now() })}\n\n`)
      );

      unsubscribe = subscribeProgress((event) => {
        if (!isConnected) return;

        try {
          controller.enqueue(
            encoder.encode(`data: ${JSON.stringify({ type: "progress", ...event })}\n\n`)
          );
        } catch {
          cleanup();
        }
      });

      heartbeatInterval = setInterval(() => {
        if (!isConnected) return;

        try {
          controller.enqueue(
            encoder.encode(`data: ${JSON.stringify({ type: "heartbeat", timestamp: Date.now() })}\n\n`)
          );
        } catch {
          cleanup();
        }
      }, 30000);

      request.signal.addEventListener("abort", cleanup);
    },

    cancel() {
      cleanup();
    },
  });

  return new Response(stream, {
    headers: {
      "Content-Type": "text/event-stream",
      "Cache-Control": "no-cache, no-transform",
      Connection: "keep-alive",
      "X-Accel-Buffering": "no",
    },
  });
}
